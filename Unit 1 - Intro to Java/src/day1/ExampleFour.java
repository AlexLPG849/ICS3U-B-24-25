public class ExampleFour {
    public static void main(String[] args) {
        // double * double = double
        // int * int = int
        // int * double = double
        
        System.out.println(5/2);
        System.out.println(5/2.0);

        int mark1 = 432;
        int mark2 = 544;
        int mark3 = 453;

        double average = (mark1 + mark2 + mark3) / 3.0;
        System.out.println(average);


    }
}
